def crc8(data: bytes) -> int:
    crc = 0
    for byte in data:
        crc = _CRC8_TABLE[(crc ^ byte) & 0xFF]
    return crc & 0xFF


def _packet(command: int, payload: bytes) -> bytes:
    n = len(payload)
    return (
        bytes([0x51, 0x78, command, 0x00, n & 0xFF, (n >> 8) & 0xFF])
        + payload
        + bytes([crc8(payload), 0xFF])
    )


def cmd_set_energy(val: int) -> bytes:
    return _packet(0xAF, bytes([(val >> 8) & 0xFF, val & 0xFF]))


def cmd_apply_energy() -> bytes:
    # Captured app payload is 00 01 (we previously sent just 01).
    return _packet(0xBE, bytes([0x00, 0x01]))


def cmd_feed_paper(lines: int) -> bytes:
    return _packet(0xBD, bytes([lines & 0xFF]))


def _byte_encode(row_bits: list[int]) -> bytes:
    """Pack 384 pixels into 48 bytes, LSB-first (leftmost pixel = bit 0)."""
    out = bytearray(ROW_BYTES)
    for i, on in enumerate(row_bits):
        if on:
            out[i >> 3] |= 1 << (i & 7)
    return bytes(out)


def _row_packet(row_bits: list[int]) -> bytes:
    # Raw fixed-length bitmap (0xA2). We send raw rather than RLE so behaviour
    # is identical regardless of content (text vs gradients).
    return _packet(0xA2, _byte_encode(row_bits))


def _image_rows(img: Image.Image) -> list[list[int]]:
    if img.width != PRINTER_WIDTH:
        raise ValueError(f"image width must be {PRINTER_WIDTH}px, got {img.width}px")
    if img.mode != "1":
        img = img.convert("1")
    px = img.load()
    return [[0 if px[x, y] else 1 for x in range(PRINTER_WIDTH)] for y in range(img.height)]


def image_to_commands(
    img: Image.Image,
    energy: int = DEFAULT_ENERGY,
    feed_lines: int = DEFAULT_FEED_LINES,
) -> bytes:
    """1-bit PIL image -> complete BLE byte stream.

    Control commands (SET_QUALITY 0x33, SET_ENERGY 0x6b12, APPLY 00 01,
    FEED 40) match the official Tiny Print app byte-for-byte — verified via
    a captured btsnoop log. Image rows use the 0xA2 raw-bitmap command.
    """
    rows = _image_rows(img)
    out = bytearray()
    out += CMD_GET_DEV_STATE
    out += CMD_SET_QUALITY_200_DPI
    out += cmd_set_energy(energy)
    out += cmd_apply_energy()
    out += CMD_LATTICE_START
    for bits in rows:
        out += _row_packet(bits)
    out += CMD_LATTICE_END
    out += cmd_feed_paper(feed_lines)
    out += CMD_SET_PAPER
    out += CMD_GET_DEV_STATE
    return bytes(out)


def chunkify(data: bytes, size: int = 180) -> list[bytes]:
    return [data[i:i + size] for i in range(0, len(data), size)]
