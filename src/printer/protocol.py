"""X6h / "cat printer" (GB0x / 51 78) BLE command encoding.

This is the protocol that DEMONSTRABLY printed on this unit — the calibration
page came out crisp with it. Faithful port of the documented reference
(rbaron/catprinter cmds.py): header 0x51 0x78, image data on characteristic
ae01, CRC-8 (poly 0x07).

Known issue we are debugging: short pages print dark, long pages fade. We do
NOT assume the cause — image_to_commands() can split a tall image into blocks
(block_lines) so we can test whether per-session length is the trigger.

Frame:  51 78 <cmd> 00 <len_lo> <len_hi> <payload...> <crc8(payload)> ff
"""
from __future__ import annotations

from PIL import Image

PRINTER_WIDTH = 384
ROW_BYTES = PRINTER_WIDTH // 8  # 48

# Static command packets. SET_QUALITY payload is 0x33 — captured from the
# official Tiny Print app (we previously sent 0x32, which was wrong).
CMD_GET_DEV_STATE = bytes([0x51, 0x78, 0xA3, 0x00, 0x01, 0x00, 0x00, 0x00, 0xFF])
CMD_SET_QUALITY_200_DPI = bytes([0x51, 0x78, 0xA4, 0x00, 0x01, 0x00, 0x33, 0x99, 0xFF])
CMD_LATTICE_START = bytes(
    [0x51, 0x78, 0xA6, 0x00, 0x0B, 0x00, 0xAA, 0x55, 0x17, 0x38, 0x44,
     0x5F, 0x5F, 0x5F, 0x44, 0x38, 0x2C, 0xA1, 0xFF]
)
CMD_LATTICE_END = bytes(
    [0x51, 0x78, 0xA6, 0x00, 0x0B, 0x00, 0xAA, 0x55, 0x17, 0x00, 0x00,
     0x00, 0x00, 0x00, 0x00, 0x00, 0x17, 0x11, 0xFF]
)
CMD_SET_PAPER = bytes([0x51, 0x78, 0xA1, 0x00, 0x02, 0x00, 0x30, 0x00, 0xF9, 0xFF])

# Energy = darkness. CAPTURED FROM THE WORKING APP: it sets energy to 0x6b12
# (27410) — bytes 6b 12 big-endian — together with SET_QUALITY=0x33 and an
# apply command of 00 01. We were never using this exact combination.
DEFAULT_ENERGY = 0x6B12
DEFAULT_FEED_LINES = 0x28  # 40, matching the app's FEED payload

